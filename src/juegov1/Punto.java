public class Punto {
    public float x,y;
    public Punto(){
        x = 0f;
        y = 0f;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = (float)x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = (float)y;
    }
    
}
